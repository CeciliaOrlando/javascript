// Tema: Operador de Asignación en JavaScript.
// El operador de asignación se utiliza para asignar un valor a una variable.
// El operador de asignación se representa con el símbolo "=".

let a;
let b = 2;

console.log(a); // undefined, no tiene valor asignado
console.log(b); // Tiene un valor asignado que es igual a 2

a = 10; // Asignar un valor a la variable a con el operador de asignación "="
console.log(a); // Tiene un valor asignado que es 10, ya no es mas undefined
