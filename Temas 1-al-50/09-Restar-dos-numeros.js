/* Tema: Restar dos números en JavaScript.
JavaScript permite realizar operaciones matemáticas básicas como la suma, resta, multiplicación y división.
En este caso, se están restando 15 y 5, 5 y 15, y 15 y 15, y los resultados se almacenan en la variable "resta".
El resultado de la resta se muestra en la consola utilizando console.log().
La función console.log() se utiliza para mostrar información en la consola del navegador o del entorno de desarrollo.
La resta de dos números puede dar como resultado un número positivo, negativo o cero.
*/

let resta = 15 - 5; // 10
console.log(resta);

resta = 5 - 15; // -10
console.log(resta);

resta = 15 - 15; // 0
console.log(resta);
