/* Tema: Multiplicar dos números con JavaScript.
JavaScript permite realizar operaciones matemáticas básicas como la suma, resta, multiplicación y división.
En este caso, se están multiplicando 5 y 3, 9 y 0, -5 y 6, 5 y -6, y -5 y -6, y los resultados se almacenan en la variable "producto".
El resultado de la multiplicación se muestra en la consola utilizando console.log().
La función console.log() se utiliza para mostrar información en la consola del navegador o del entorno de desarrollo.
La multiplicación de dos números puede dar como resultado un número positivo, negativo o cero.
La multiplicación de un número por cero siempre da como resultado cero.
La multiplicación de dos números negativos da como resultado un número positivo.
La multiplicación de un número positivo por un número negativo da como resultado un número negativo.
La multiplicación de un número negativo por un número positivo da como resultado un número negativo.
La multiplicación de dos números positivos da como resultado un número positivo.
*/

let producto = 5 * 3; // 15
console.log(producto);

producto = 9 * 0; //  0
console.log(producto);

producto = -5 * 6; // -30
console.log(producto);

producto = 5 * -6; // -30
console.log(producto);

producto = -5 * -6; //
console.log(producto);
