/* Tema: Dividir dos números en JavaScript.
JavaScript permite realizar operaciones matemáticas básicas como la suma, resta, multiplicación y división.
En este caso, se están dividiendo 20 y 2, 17 y 31, y 3 y 0, y los resultados se almacenan en la variable "cociente".
El resultado de la división se muestra en la consola utilizando console.log().
La función console.log() se utiliza para mostrar información en la consola del navegador o del entorno de desarrollo.
La división de dos números puede dar como resultado un número positivo, negativo o cero.
La división de un número por cero da como resultado infinito.
*/

let cociente = 20 / 2;
console.log(cociente); // 10

cociente = 17 / 31; // 0.5483870967741935
console.log(cociente);

cociente = 3 / 0; // Infinity
console.log(cociente);
