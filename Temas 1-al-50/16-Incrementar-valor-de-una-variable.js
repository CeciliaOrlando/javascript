/* Tema: Incrementar el valor de una variable en JavaScript.
*/

let librosComprados = 105;
console.log(librosComprados); // Inicialmente tenemos 105 libros comprados

// Opción 1

librosComprados = librosComprados + 1; // 106
console.log(librosComprados);

// Opción 2

librosComprados++; // 107
console.log(librosComprados);
