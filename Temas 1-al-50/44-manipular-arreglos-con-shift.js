/* Tema: Manipular arreglos con .shift().
El método .shift() se utiliza para eliminar el primer elemento de un arreglo.
El método .shift() devuelve el elemento eliminado y modifica el arreglo original.
*/

var estaciones = ["Invierno", "Otoño", "Primavera"];

estaciones.shift();  // Remover primer elemento del arreglo

console.log(estaciones); // Otoño, Primavera
