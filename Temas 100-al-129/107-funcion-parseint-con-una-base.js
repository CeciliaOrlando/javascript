/* Tema: Función "parseInt" con una base (radix).
La funcion parseInt() se utiliza para convertir una cadena de texto en un número entero.
La base (radix) se utiliza para especificar el sistema de numeración utilizado en la cadena de texto.
*/

console.log(parseInt("110111", 2)); // Binario que siempre empieza con 0b 
console.log(parseInt(110111, 2));   // Binario que siempre empieza con 0b

console.log(parseInt("3E0A", 16));  // Hexadecimal que siempre empieza con 0x
