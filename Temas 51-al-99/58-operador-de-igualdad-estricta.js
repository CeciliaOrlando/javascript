/* Tema: Operador de igualdad estricta.
El operador de igualdad estricta (===) compara tanto el valor como el tipo de dato.
*/

console.log(9 == 9);   // true , son el mimso tipo de dato y valor
console.log(9 == "9"); // true , si bien son el mismo valor, son distintos tipos de dato

console.log(9 === 9);   // true , son el mimso tipo de dato y valor
console.log(9 === "9"); // false , son distintos tipos de dato
