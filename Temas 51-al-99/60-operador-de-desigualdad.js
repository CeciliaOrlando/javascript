/* Tema: Operador de desigualdad.
El operador de desigualdad (!=) compara tanto el valor como el tipo de dato.
*/

console.log(9 != 6);  // true  9 es diferente que 6 por eso es true

console.log(9 != 9);  // false  9 es igual que 9 por eso es false, no son distintos son iguales porque tienen el mismo valor

console.log("JavaScript" != "JavaScript"); // false JavaScript es igual que JavaScript por eso es false

console.log([1, 2, 3] != [1, 2, 3]); // true porque son distintos objetos. No compara si son iguales

// != es un operador de desigualdad que signdifica "no es igual a"
