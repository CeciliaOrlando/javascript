/* Tema: Operador "menor que" en JavaScript. */

// Menor que: <

console.log(5 < 6);  // true  5 es menor que 6
console.log(10 < 3); // false  10 es mayor que 3

console.log("A" < "B");     // true  A es menor que B
console.log("ABC" < "ACB"); // true  ABC es menor que ACB

var a = 15;
var b = 7;

console.log(a < b);  // false  15 es mayor que 7
console.log(b < a);  // true   7 es menor que 15
