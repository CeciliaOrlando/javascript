/* En JavaScript, las variables son contenedores para almacenar datos.
Se pueden declarar variables usando var, let o const.
let permite declarar variables que pueden cambiar su valor.
const permite declarar variables que no pueden cambiar su valor.
var es una forma más antigua de declarar variables y tiene un alcance diferente.
Se recomienda usar let o const en su lugar.*/

/* Tipos de Datos:
undefined, null, boolean, string, symbol, number, y object.
UNDEFINED: Tipo de dato que no tiene valor asignado.
NULL: Tipo de dato que representa la ausencia de un valor.
BOOLEAN: Tipo de dato que representa verdadero o falso.
STRING: Tipo de dato que representa una cadena de texto.
SYMBOL: Tipo de dato que representa un valor único e inmutable.
NUMBER: Tipo de dato que representa números enteros o decimales.
OBJECT: Tipo de dato que representa un objeto, que puede contener múltiples valores y propiedades. */

